    private void btnIndexActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIndexActionPerformed
        // TODO add your handling code here:

        //Busqueda indexada
        if (!existeArchivo()) {
            showMessageDialog(null, "DEBES GENERAR EL ARCHIVO", "GENERAR ARCHIVO", ERROR_MESSAGE);

        } else if (campoVacio(this.txtCIndex.getText())) {
            showMessageDialog(null, "CAMPO VACIO ", "BUSQUEDA", ERROR_MESSAGE);
        } else {
            //Se mide el tiempo de ejecución
            long startTime = System.nanoTime();
            String r = busquedaIndexada();
            long endTime = System.nanoTime() - startTime; // tiempo en que se ejecuta su método
            if (r.equals("No")) {
                showMessageDialog(null, "NO SE ENCONTRÓ CLAVE EN EL ARCHIVO", "ARCHIVO", ERROR_MESSAGE);
            } else {

                this.r_Index.setText(r);
                //this.t_index.setText(String.valueOf(endTime));
            }
            this.t_index.setText(String.valueOf(endTime));
        }
    }//GEN-LAST:event_btnIndexActionPerformed

