    /*Genera un nuevo archivo (si ya existe lo sobre-escribe)*/
    public boolean generarArchivo() {

        if (this.txtNum.getText().equals("")) {
            showMessageDialog(null, "REVISE LA INFORMACIÓN", "GENERAR ARCHIVO", ERROR_MESSAGE);
        } else {
            int numReg = Integer.parseInt(this.txtNum.getText());
            boolean flag = F.generarArchivo(this.getArchivo(), numReg);
            return flag;
        }

        return false;

    }

