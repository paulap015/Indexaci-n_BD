        if (!existeArchivo()) {
            showMessageDialog(null, "DEBES GENERAR EL ARCHIVO", "GENERAR ARCHIVO", ERROR_MESSAGE);
        } else if (campoVacio(this.newClave.getText()) == false && campoVacio(this.newDato.getText()) == false) {
           
            /*Validar rango de los regisros {clave:valor}*/
            int key = Integer.parseInt(this.newClave.getText());
            int valor = Integer.parseInt(this.newDato.getText());

            if (key <= 1000000 && key >= 0 && valor <= 1000000 && valor >= 0) {
                if (F.agregarNuevoElemento(this.getArchivo(), this.getCopiaArchivo(), this.newClave.getText(), this.newDato.getText())) {
                    showMessageDialog(null, "DATO AGREGADO EXITOSAMENTE ");
                } else {
                    showMessageDialog(null, "CLAVE EXISTENTE", "ERORR", ERROR_MESSAGE);
                }
            } else {
                showMessageDialog(null, "FUERA DE RANGO", "ERORR", ERROR_MESSAGE);
            }

            this.newClave.setText("");
            this.newDato.setText("");
        } else {
            showMessageDialog(null, "CAMPO VACIO ", "NO SE GENERÓ REGISTRO", ERROR_MESSAGE);
        }

